package mk.ukim.finki.aud0;

public class MyFirstClass {

    public static void main(String[] args) {

        //println prints in a new line
        System.out.println("This will be shown in a new line.");

        //string concat can be done using +
        System.out.println("This is one " + "string.");

        //formatted printing can be done using printf
        System.out.printf("The value of the PI constant is: %.2f.", Math.PI);

        //print method prints in the same line
        System.out.print("**********");
    }
}
